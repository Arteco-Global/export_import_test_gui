function interpolate(template, params = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => {
    return Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : `{${key}}`;
  });
}

export function getLanguage() {
  return state.language;
}

export function t(key, params = {}) {
  const lang = getLanguage();
  const fallback = TRANSLATIONS.it[key] ?? key;
  const template = TRANSLATIONS[lang]?.[key] ?? fallback;
  return interpolate(template, params);
}

function applyTranslationsToDom() {
  document.documentElement.lang = getLanguage();
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.setAttribute("placeholder", t(node.dataset.i18nPlaceholder));
  });
  document.querySelectorAll("[data-i18n-html]").forEach((node) => {
    node.innerHTML = t(node.dataset.i18nHtml);
  });
  document.title = t("appTitle");
}

export function onLanguageChange(callback) {
  listeners.add(callback);
  return () => listeners.delete(callback);
}

export function setLanguage(lang) {
  const nextLang = SUPPORTED_LANGS.has(lang) ? lang : "it";
  state.language = nextLang;
  window.localStorage.setItem(STORAGE_KEY, nextLang);

  const select = document.getElementById("languageSelect");
  if (select && select.value !== nextLang) {
    select.value = nextLang;
  }

  applyTranslationsToDom();
  listeners.forEach((callback) => callback(nextLang));
}

export function initI18n() {
  const storedLang = window.localStorage.getItem(STORAGE_KEY);
  const initialLang = SUPPORTED_LANGS.has(storedLang) ? storedLang : "it";

  const languageSelect = document.getElementById("languageSelect");
  if (languageSelect) {
    languageSelect.addEventListener("change", (event) => {
      setLanguage(event.target.value);
    });
  }

  setLanguage(initialLang);
}
